import { useState, useCallback } from 'react';
import { Sparkles, PlayCircle, Loader2, Zap, Moon, Target, Music, Coffee, PartyPopper } from 'lucide-react';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Badge } from '@/components/ui/badge';
import { ScrollArea } from '@/components/ui/scroll-area';
import { toast } from 'sonner';
import { useAuth } from '@/contexts/AuthContext';
import { createPlaylist, addTracksToPlaylist, getCurrentUser } from '@/lib/spotify-api';
import { cn } from '@/lib/utils';

export interface ContextConfig {
  key: string;
  name: string;
  description: string;
  icon: React.ReactNode;
  bpmStart: number;
  bpmEnd: number;
  energyStart: number;
  energyEnd: number;
  undergroundRatio: number;
  progression: 'build' | 'plateau' | 'descend' | 'steady';
}

export interface GeneratedTrack {
  track_id: string;
  name: string;
  artist: string;
  tempo: number | null;
  energy: number | null;
  popularity: number | null;
  position: number;
}

interface ContextPlaylistGeneratorProps {
  onGenerate: (context: ContextConfig, durationMinutes: number) => Promise<GeneratedTrack[]>;
}

